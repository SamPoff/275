#include "Point.h"

/** Default Point constructor */
Point::Point() {
    x = 0;
    y = 0;
}//end default constructor

/** Parameter Point constructor
@param Xa - X value passed in by program.
@param Ya - Y value passed in by program. */
Point::Point(int Xa, int Ya) {
    x = Xa;
    y = Ya;
}//end constructor


